package java_0517;

public class Op2 {

	public static void main(String[] args) {
		// 증감연산자 => ++ , --
		
		int x = 0;
		System.out.println(x);
		
		x = x+1;
		System.out.println(x);
		x++;
		System.out.println(x);
		
		int y = 17;
		System.out.println(y);
		
		y = y-1;
		System.out.println(y);
		y--;
		System.out.println(y);
			
	}

}
