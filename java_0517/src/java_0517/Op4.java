package java_0517;

public class Op4 {

	public static void main(String[] args) {
		// 복합대입연산자 =>  += , -= , *= , /= , %=
		
		int x = 100;
		
		x += 10;
		System.out.println(x);
		
		x -= 20;
		System.out.println(x);
		
		x *= 3;
		System.out.println(x);
		
		x /= 9;
		System.out.println(x);
		
		x %= 17;
		System.out.println(x);

	}

}
