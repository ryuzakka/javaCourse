package java_0517;

public class Comp1 {

	public static void main(String[] args) {
		// 비교연산자 => 2개의 요소를 비교 => 결과는 true, false
		
		System.out.println(10 > 20);
		System.out.println(10 < 20);
		System.out.println(10 == 20);
		System.out.println(10 <= 20);
		System.out.println(10 >= 20);
		System.out.println(10 != 20);

	}

}
