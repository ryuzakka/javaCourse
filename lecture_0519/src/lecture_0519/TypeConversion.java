package lecture_0519;

public class TypeConversion {

	public static void main(String[] args) {
		// 자료 형 변환
		
		byte a = 0;
		short b = 0;
		int c = 0;
		long d = 0;
		
		byte a1 = a;
		short a2 = a;
		int a3 = a;
		long a4 = a;
		
		byte b1 = (byte)b;
		short b2 = b;
		int b3 = b;
		long b4 = b;
		
		byte c1 = (byte)c;
		short c2 = (short)c;
		int c3 = c;
		long c4 = c;

		byte d1 = (byte)d;
		short d2 = (short)d;
		int d3 = (int)d;
		long d4 = d;
	}

}
